var sigCert, encCert dtlcp.Certificate

func init() {
	var err error
	sigCert, err = dtlcp.X509KeyPair([]byte(SIG_CERT_PEM), []byte(SIG_KEY_PEM))
	if err != nil {
		panic(err)
	}
	encCert, err = dtlcp.X509KeyPair([]byte(ENC_CERT_PEM), []byte(ENC_KEY_PEM))
	if err != nil {
		panic(err)
	}
}

func main() {
	config := &dtlcp.Config{
		// Certificates 为空时，使用 GetCertificate / GetKECertificate 动态选择证书
		// GetCertificate 根据客户端 TrustedCAIndications 选择签名证书
		GetCertificate: func(info *dtlcp.ClientHelloInfo) (*dtlcp.Certificate, error) {
			fmt.Printf(">>> 收到 ClientHello, SNI=%q, TrustedCAIndications=%d\n",
				info.ServerName, len(info.TrustedCAIndications))
			for i, ca := range info.TrustedCAIndications {
				fmt.Printf("  TrustedCA[%d]: Type=%d, Len=%d\n", i, ca.IdentifierType, len(ca.Identifier))
			}
			// 实际场景中可根据 TrustedCAIndications 内容选择对应的签名证书返回。
			// 本例仅演示接口用法，不论客户端指示哪个 CA，都返回默认签名证书。
			return &sigCert, nil
		},
		// GetKECertificate 根据客户端 TrustedCAIndications 选择加密证书
		GetKECertificate: func(info *dtlcp.ClientHelloInfo) (*dtlcp.Certificate, error) {
			fmt.Printf(">>> 选择加密证书, TrustedCAIndications=%d\n", len(info.TrustedCAIndications))
			// 若客户端指示了特定的 CA，可据此选择对应的加密证书。
			// 例如，检查 TrustedCAIndications 中是否包含特定的 CA 标识符，
			// 若匹配则返回对应的加密证书，否则返回默认证书。
			// 本例简化处理，始终返回默认加密证书。
			return &encCert, nil
		},
	}
	ln, err := dtlcp.Listen("udp", ":8450", config)
	if err != nil {
		panic(err)
	}
	defer ln.Close()
	for {
		conn, err := ln.Accept()
		if err != nil {
			panic(err)
		}
		go func() {
			defer conn.Close()
			buf := make([]byte, 1024)
			_, _ = conn.Read(buf)
			conn.Write([]byte("Hello DTLCP Multi Cert Client!"))
		}()
	}
}
